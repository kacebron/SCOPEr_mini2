#' Generate or modify input_data_default.csv content
#'
#' This function creates the template of input_data_default.csv from scratch
#' and replaces parameter values with user-specified inputs.
#' It returns the full CSV content as a character vector, so that the user
#' can write it to a file with writeLines().
#'
#' @export
input_data_default <- function(
  Cab = 40, Cca = 10, Cdm = 0.012, Cw = 0.009, Cs = 0, Cant = 1,
  Cp = 0, Cbc = 0, N = 1.5, rho_thermal = 0.01, tau_thermal = 0.01,
  Vcmax25 = 60, BallBerrySlope = 8, BallBerry0 = 0.01, Type = 0, kV = 0.64,
