  Cv = 1, crowndiameter = 1,
  z = 5, Rin = 600, Ta = 20, Rli = 300, p = 970, ea = 15, u = 2,
  Ca = 410, Oa = 209,
  zo = 0.25, d = 1.34, Cd = 0.3, rb = 10, CR = 0.35, CD1 = 20.6,
  Psicor = 0.2, CSSOIL = 0.01, rbs = 10, rwc = 0,
  startDOY = 20060618, endDOY = 20300101, LAT = 51.55, LON = 5.55,
  timezn = 1, tts = 30, tto = 0, psi = 0
) {
  template <- c(
    "PROSPECT,",
    paste0("Cab,", Cab),
    paste0("Cca,", Cca),
    paste0("Cdm,", Cdm),
    paste0("Cw,", Cw),
    paste0("Cs,", Cs),
    paste0("Cant,", Cant),
    paste0("Cp,", Cp),
    paste0("Cbc,", Cbc),
    paste0("N,", N),
    paste0("rho_thermal,", rho_thermal),
    paste0("tau_thermal,", tau_thermal),
    ",",
    "Leaf_Biochemical,",
    paste0("Vcmax25,", Vcmax25),
    paste0("BallBerrySlope,", BallBerrySlope),
    paste0("BallBerry0,", BallBerry0),
    paste0("Type,", Type),
    paste0("kV,", kV),
    paste0("Rdparam,", Rdparam),
    paste0("Kn0,", Kn0),
    paste0("Knalpha,", Knalpha),
    paste0("Knbeta,", Knbeta),
    " ,",
    "Leaf_Biochemical_magnani,",
    paste0("Tyear,", Tyear),
    paste0("beta,", beta),
    paste0("kNPQs,", kNPQs),
    paste0("qLs,", qLs),
    paste0("stressfactor,", stressfactor),
    ",",
    "Fluorescence,",
    paste0("fqe,", fqe),
    ",",
    "Soil,",
    paste0("spectrum,", spectrum),
    paste0("rss,", rss),
    paste0("rs_thermal,", rs_thermal),
    paste0("cs,", cs),
    paste0("rhos,", rhos),
    paste0("lambdas,", lambdas),
    paste0("SMC,", SMC),
    paste0("BSMBrightness,", BSMBrightness),
    paste0("BSMlat,", BSMlat),
    paste0("BSMlon,", BSMlon),
    ",",
    "Canopy,",
    paste0("LAI,", LAI),
    paste0("hc,", hc),
    paste0("LIDFa,", LIDFa),
    paste0("LIDFb,", LIDFb),
    paste0("leafwidth,", leafwidth),
    paste0("Cv,", Cv),
    paste0("crowndiameter,", crowndiameter),
    ",",
    "Meteo,",
    paste0("z,", z),
    paste0("Rin,", Rin),
    paste0("Ta,", Ta),
    paste0("Rli,", Rli),
    paste0("p,", p),
    paste0("ea,", ea),
    paste0("u,", u),
    paste0("Ca,", Ca),
    paste0("Oa,", Oa),
    ",",
    "Aerodynamic,",
    paste0("zo,", zo),
    paste0("d,", d),
    paste0("Cd,", Cd),
    paste0("rb,", rb),
    paste0("CR,", CR),
    paste0("CD1,", CD1),
    paste0("Psicor,", Psicor),
    paste0("CSSOIL,", CSSOIL),
    paste0("rbs,", rbs),
    paste0("rwc,", rwc),
    ",",
    "timeseries,",
    paste0("startDOY,", startDOY),
    paste0("endDOY,", endDOY),
    paste0("LAT,", LAT),
    paste0("LON,", LON),
    paste0("timezn,", timezn),
    ",",
    "Angles,",
    paste0("tts,", tts),
    paste0("tto,", tto),
    paste0("psi,", psi)
  )
  return(template)
}

